#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*new;

	i = 0;
	new = malloc(1 + (ft_strlen((char *)s)) * sizeof(char));
	if (new == NULL)
	{
		return (NULL);
	}
	while (s[i] != '\0')
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
